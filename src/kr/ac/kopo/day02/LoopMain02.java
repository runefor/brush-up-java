package kr.ac.kopo.day02;

public class LoopMain02 {
    public static void main(String[] args) {
        for (int i = 0; i < 6; i++) {
            System.out.println("몇개 찍는다.");
        }

        // 시작과 끝이 있거나, 횟수가 있는 곳에 씀.
        for (int j = 0; j < 6; j++) {
            System.out.println("하ㅏ하하하하하ㅏ하하하ㅏ하");
        }
    }
}
